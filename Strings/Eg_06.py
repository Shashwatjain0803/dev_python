s1 = "Hello World Hello Python"
print(s1.split())

s2 = "Hello Java"
print(s2.split(" "))

s3 = "Hello-Java-Hello-Python"
print(s3.split("-"))

s4 = "Hello:Java:Hello:Python"
print(s4.split(":"))

s5 = "Hello Java and Hello Python and Hello JavaScript"
print(s5.split('and'))

s6 = "Hello Java and Hello Python and Hello JavaScript"
print(s6.split('and', 1))